package hafta1;
//Java Dersi 13 : Do-While Dongusuyle Calismak

public class DoWhileDongusu {

    public static void main(String[] args) {
        int i = 2;
        do {
            System.out.println(i);
            i += 2;
        } while (i < 10);
        System.out.println("do while dongusu bitti");
    }
}
